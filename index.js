const books = require("./index.json");

/*
================================================================================
Your task is to write a function that takes in this array of book
objects and returns an array of all the titles of books that are available.

The returned array should have the following structure:
[
  "The Great Gatsby",
  "Pride and Prejudice",
  "To Kill a Mockingbird",
  // ...
]
================================================================================
*/
function getAvailableBooks(books) {
  return books.map((book) => {
    return book.title;
  });
}

console.log(getAvailableBooks(books));
